/**
 * Variabel ada 2 yaitu var dan val
 * var merupakan variabel yang nilai atau literal nya bisa dirubah ubah.
 * sedangkan val merupakan variabel yang nilai atau literalnya tidak bisa diubah lagi.
 * biasanya digunakan untuk konstanta.
 *
 */

fun main() {
    var angka = 5
    angka = 10 //var bisa dirubah
    println(angka)


    val PHI = 3.14
    // PHI = 22.9   => eror tidak bisa dirubah
    println(PHI)
}